package javaa.lab2.Q2.PackageP1;

public class two {
   int x;
   int y;
   
    public two(int x, int y) {
         this.x = x;
         this.y = y;
    }
    
    public two() {
        this.x = 0;
        this.y = 0;
    }

    public String toString() {
        return "x: " + x + " y: " + y;
    }
}
